function mulberry32(a: number) {
  return function () {
    a |= 0; a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function makePattern(seed: number): number[][] {
  const N = 11, density = 0.46;
  const r = mulberry32(((seed * 2654435761) >>> 0) || 1);
  const grid = Array.from({ length: N }, () => new Array(N).fill(0));
  const choice = Math.floor(r() * 3);
  const half = Math.ceil(N / 2);
  for (let y = 0; y < N; y++)
    for (let x = 0; x < half; x++) {
      const v = r() < density ? 1 : 0;
      grid[y][x] = v;
      grid[y][N - 1 - x] = v;
    }
  const stamp = (ox: number, oy: number) => {
    const s = 5;
    for (let i = -1; i <= s; i++)
      for (let j = -1; j <= s; j++) {
        const py = oy + i, px = ox + j;
        if (py >= 0 && py < N && px >= 0 && px < N) grid[py][px] = 0;
      }
    for (let i = 0; i < s; i++)
      for (let j = 0; j < s; j++) {
        const edge = i === 0 || j === 0 || i === s - 1 || j === s - 1;
        const center = i >= 2 && i <= s - 3 && j >= 2 && j <= s - 3;
        grid[oy + i][ox + j] = edge || center ? 1 : 0;
      }
  };
  if (choice === 0 || choice === 1) { stamp(0, 0); stamp(N - 5, 0); }
  if (choice === 1) { stamp(0, N - 5); stamp(N - 5, N - 5); }
  return grid;
}

function buildFigureGrid(figs: string[][], index: number, showFrame: boolean): number[][] {
  const G = 15, off = 2, N = 11;
  const fig = figs[index % figs.length];
  const grid = Array.from({ length: G }, () => new Array(G).fill(0));
  for (let y = 0; y < N; y++) {
    const row = fig[y] || "";
    for (let x = 0; x < N; x++) if (row[x] === "#") grid[off + y][off + x] = 1;
  }
  if (showFrame) {
    const corners = [[0, 0], [G - 2, 0], [0, G - 2], [G - 2, G - 2]];
    corners.forEach(([cx, cy]) => {
      for (let i = 0; i < 2; i++) for (let j = 0; j < 2; j++) grid[cy + i][cx + j] = 1;
    });
    for (let i = 4; i <= G - 5; i += 2) {
      grid[0][i] = 1; grid[G - 1][i] = 1; grid[i][0] = 1; grid[i][G - 1] = 1;
    }
  }
  return grid;
}

/** Grille de modules (0/1) pour un modèle donné. */
export function currentGrid(family: Family, index: number, showFrame: boolean): number[][] {
  if (family === "patterns") return makePattern(index + 1);
  const figs = family === "wild" ? FIG_WILD : FIG_FIGURES;
  return buildFigureGrid(figs, index, showFrame);
}

/** SVG plat 1024×1024 prêt à exporter / sauvegarder (fond + modules). */
export function buildExportSvg(cfg: AvatarConfig): string {
  const S = 1024, pad = 0.13, area = 1 - pad * 2;
  const grid = currentGrid(cfg.family, cfg.index, cfg.showFrame);
  const G = grid.length, span = G + 2;
  const k = area / span;
  const { gap, moduleShape: shape } = cfg;
  const rxFrac = shape === "dot" ? (1 - gap) / 2 : shape === "rounded" ? 0.2 : 0;
  let rects = "";
  for (let y = 0; y < G; y++)
    for (let x = 0; x < G; x++) {
      if (!grid[y][x]) continue;
      const ex = (pad + (x + gap / 2 + 1) * k) * S;
      const ey = (pad + (y + gap / 2 + 1) * k) * S;
      const w = (1 - gap) * k * S;
      const rx = rxFrac * k * S;
      rects += `<rect x="${ex.toFixed(2)}" y="${ey.toFixed(2)}" width="${w.toFixed(2)}" height="${w.toFixed(2)}" rx="${rx.toFixed(2)}" fill="${cfg.fg}"/>`;
    }
  const isCircle = cfg.avatarShape === "circle";
  const clipDef = isCircle ? `<clipPath id="clip"><circle cx="${S / 2}" cy="${S / 2}" r="${S / 2}"/></clipPath>` : "";
  const clip = isCircle ? ` clip-path="url(#clip)"` : "";
  const bgRx = isCircle ? 0 : Math.round(0.13 * S);
  const bg = `<rect x="0" y="0" width="${S}" height="${S}" rx="${bgRx}" fill="${cfg.bg}"/>`;
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${S}" height="${S}" viewBox="0 0 ${S} ${S}"><defs>${clipDef}</defs><g${clip}>${bg}${rects}</g></svg>`;
}
